/** 検索クエリに一致する科目一覧（漢字部分一致 or よみがな部分一致。カタカナはひらがなに正規化） */
export function searchAccounts(query) {
  const q = query.trim().replace(/[ァ-ヶ]/g, ch => String.fromCharCode(ch.charCodeAt(0) - 0x60));
  if (!q) return [];
  return ALL_ACCOUNTS.filter(a =>
    a.includes(query.trim()) || (ACCOUNT_READINGS[a] ?? '').includes(q)
  );
}
